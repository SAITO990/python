
#type() es una función integrada que se utiliza para devolver el tipo de datos almacenados en los objetos o variables del programa.
print(type(9))
print(type(10.1))

# OPERADORES ARITMETICOS

# + Realiza Adición entre los operandos
print(12 + 3)

# - Realiza Substracción entre los operandos
print(12 - 3)

# * Realiza Multiplicación entre los operandos
print(12 * 3)

# / Realiza División entre los operandos
print(12 / 3)

# % Realiza un módulo entre los operandos
print(16 % 3)

# ** Realiza la potencia de los operandos
print(12 ** 3)

# // Realiza la división con resultado de número entero
print(18 // 5)



# OPERADORES RELACIONALES

# > Devuelve True si el operador de la izquierda es mayor que el operador de la derecha
print(12 > 3)

# < Devuelve True si el operador de la derecha es mayor que el operador de la izquierda
print(12 < 3)

# == Devuelve True si ambos operandos son iguales\
print(12 == 3)

# >= Devuelve True si el operador de la izquierda es mayor o igual que el operador de la derecha
print(12 >= 3)

# <= Devuelve True si el operador de la derecha es mayor o igual que el operador de la izquierda
print(12 <= 3)

# != Devuelve True si ambos operandos no son iguales
print(12 != 3)   



